        bash ~/vault_sync_setup.sh
        echo "✅ Vault synchronization set up"
    else
        echo "⚠️  Vault sync script not found, skipping"
    fi
    
    # Step 7: Commit and push changes
    echo ""
    echo "Step 7: Committing and pushing changes..."
    
    local timestamp="$(date +%Y-%m-%d\ %H:%M:%S)"
    local commit_message="Framework setup: $timestamp"
    
    for repo_root in "${REPO_ROOTS[@]}"; do
        local repo_name=$(basename "$repo_root")
        commit_and_push_changes "$repo_root" "$repo_name" "$commit_message"
    done
    
    echo "✅ Changes committed and pushed"
    
    # Step 8: Final verification
    echo ""
    echo "Step 8: Final verification..."
    
