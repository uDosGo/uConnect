# Function to set proper permissions
set_proper_permissions() {
    local dir_path="$1"
    local owner="$2"
    local group="$3"
    
    echo "Setting permissions for $dir_path..."
    
    # Set directory permissions
    chmod 755 "$dir_path"
    
    # Set ownership if specified
    if [ -n "$owner" ]; then
        sudo chown -R "$owner:$group" "$dir_path" 2>/dev/null || chown -R "$owner:$group" "$dir_path"
    fi
    
    # Set special permissions for sensitive directories
    if [[ "$dir_path" == *"private"* ]] || [[ "$dir_path" == *"secrets"* ]]; then
        chmod 700 "$dir_path"
        echo "✅ Secure permissions set for $dir_path (700)"
    else
        echo "✅ Standard permissions set for $dir_path (755)"
    fi
}

