# Function to commit and push changes
commit_and_push_changes() {
    local repo_path="$1"
    local repo_name="$2"
    local commit_message="$3"
    
    echo "Committing and pushing changes for $repo_name..."
    
    cd "$repo_path"
    
    # Check if there are changes
    if [ -z "$(git status --porcelain)" ]; then
        echo "✅ No changes to commit in $repo_name"
        return
    fi
    
    # Add all changes
    git add .
    
    # Commit changes
    git commit -m "$commit_message"
    
    # Push to remote if available
    if git remote | grep -q "origin"; then
        git push origin "$(git rev-parse --abbrev-ref HEAD)"
        echo "✅ Changes pushed to remote for $repo_name"
    else
        echo "✅ Changes committed locally for $repo_name (no remote configured)"
    fi
}

# Main setup function
main_setup() {
    echo "Starting uDosGo Ecosystem Framework setup..."
    
    # Step 1: Check prerequisites
    echo "Step 1: Checking prerequisites..."
    
    if ! command_exists git; then
        echo "Error: Git is required but not installed"
        exit 1
    fi
    
    if ! command_exists curl; then
        echo "Error: curl is required but not installed"
        exit 1
    fi
    
    echo "✅ Prerequisites satisfied"
    
    # Step 2: Set up repository roots
    echo ""
    echo "Step 2: Setting up repository roots..."
    
    for repo_root in "${REPO_ROOTS[@]}"; do
        if [ ! -d "$repo_root" ]; then
            echo "Creating repository root: $repo_root"
            mkdir -p "$repo_root"
        else
            echo "✅ Repository root exists: $repo_root"
        fi
        
        set_proper_permissions "$repo_root" "$USER" "$USER"
    done
    
    echo "✅ Repository roots set up"
    
    # Step 3: Set up repository structures
    echo ""
    echo "Step 3: Setting up repository structures..."
    
    for repo_root in "${REPO_ROOTS[@]}"; do
        local repo_name=$(basename "$repo_root")
        setup_repository_structure "$repo_root" "$repo_name"
    done
    
    echo "✅ Repository structures set up"
    
    # Step 4: Initialize git repositories
    echo ""
    echo "Step 4: Initializing git repositories..."
    
    for repo_root in "${REPO_ROOTS[@]}"; do
        local repo_name=$(basename "$repo_root")
        default_git_init "$repo_root" "$repo_name"
    done
    
    echo "✅ Git repositories initialized"
    
    # Step 5: Set up framework components
    echo ""
    echo "Step 5: Setting up framework components..."
    
    local framework_path="~/uDosGo/framework"
    setup_framework_components "$framework_path"
    
    echo "✅ Framework components set up"
    
    # Step 6: Set up Vault synchronization
    echo ""
    echo "Step 6: Setting up Vault synchronization..."
    
