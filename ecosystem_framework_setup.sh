#!/bin/bash
# uDosGo Framework Core
# Core framework functions for ecosystem management

# Framework version
FRAMEWORK_VERSION="1.0.0"

# Core framework functions
framework_init() {
    echo "Initializing uDosGo Framework v$FRAMEWORK_VERSION"
    
    # Load configuration
    if [ -f "$FRAMEWORK_CONFIG" ]; then
        source "$FRAMEWORK_CONFIG"
    fi
    
    # Set up environment
    setup_environment
    
    # Initialize logging
    init_logging
    
    echo "✅ Framework initialized"
}

setup_environment() {
    # Set default environment variables
    export UDOSGO_FRAMEWORK_ROOT="${UDOSGO_FRAMEWORK_ROOT:-$HOME/uDosGo/framework}"
    export UDOSGO_CONFIG_DIR="${UDOSGO_CONFIG_DIR:-$UDOSGO_FRAMEWORK_ROOT/config}"
    export UDOSGO_LOGS_DIR="${UDOSGO_LOGS_DIR:-$UDOSGO_FRAMEWORK_ROOT/logs}"
    
    # Create directories if they don't exist
    mkdir -p "$UDOSGO_CONFIG_DIR"
    mkdir -p "$UDOSGO_LOGS_DIR"
    
    # Add to PATH
    export PATH="$PATH:$UDOSGO_FRAMEWORK_ROOT/scripts"
    export PATH="$PATH:$UDOSGO_FRAMEWORK_ROOT/tools"
}

init_logging() {
    # Set up logging
    export LOG_FILE="$UDOSGO_LOGS_DIR/framework_$(date +%Y%m%d).log"
    mkdir -p "$(dirname "$LOG_FILE")"
    touch "$LOG_FILE"
    
    log_message "Framework initialization started"
}

log_message() {
    local message="$1"
    local timestamp="$(date +'%Y-%m-%d %H:%M:%S')"
    echo "[$timestamp] $message" | tee -a "$LOG_FILE"
}

# Configuration management
load_config() {
    local config_file="$1"
    if [ -f "$config_file" ]; then
        log_message "Loading configuration from $config_file"
        source "$config_file"
    else
        log_error "Configuration file not found: $config_file"
    fi
}

save_config() {
    local config_file="$1"
    local config_content="$2"
    
    log_message "Saving configuration to $config_file"
    echo "$config_content" > "$config_file"
    chmod 600 "$config_file"
}

# Error handling
log_error() {
    local message="$1"
    local timestamp="$(date +'%Y-%m-%d %H:%M:%S')"
    echo "[$timestamp] ERROR: $message" | tee -a "$LOG_FILE" >&2
}

handle_error() {
    local exit_code="$1"
    local message="$2"
    
    if [ "$exit_code" -ne 0 ]; then
        log_error "$message (exit code: $exit_code)"
        exit "$exit_code"
    fi
}

# Repository management
clone_repository() {
    local repo_url="$1"
    local target_dir="$2"
    local branch="${3:-main}"
    
    log_message "Cloning repository: $repo_url"
    
    if [ -d "$target_dir/.git" ]; then
        log_message "Repository already exists, pulling latest changes"
        cd "$target_dir"
        git pull origin "$branch"
    else
        git clone -b "$branch" "$repo_url" "$target_dir"
    fi
    
    handle_error "$?" "Failed to clone repository: $repo_url"
}

# File management
backup_file() {
    local source="$1"
    local backup_dir="${2:-$UDOSGO_LOGS_DIR/backups}"
    
    mkdir -p "$backup_dir"
    local backup_file="$backup_dir/$(basename "$source").backup_$(date +%Y%m%d_%H%M%S)"
    
    log_message "Backing up $source to $backup_file"
    cp -p "$source" "$backup_file"
    
    handle_error "$?" "Failed to backup file: $source"
}

